const BIDIR_MARKER = {
  type:   MarkerType.ArrowClosed,
  width:  14,
  height: 14,
};

/** 양방향 엣지 쌍 생성 — markerStart + markerEnd 로 ↔ 시각화 */
function bi(
  id: string,
  src: string,
  tgt: string,
  weight: number,
): Edge<TransferEdgeData>[] {
  return [
    {
      id:          `${id}-f`,
      source:      src,
      target:      tgt,
      type:        'transfer',
      markerStart: BIDIR_MARKER,
      markerEnd:   BIDIR_MARKER,
      data:        { relationId: `${id}-f`, weight, hidden: false },
    },
    {
      id:          `${id}-r`,
      source:      tgt,
      target:      src,
      type:        'transfer',
      markerStart: BIDIR_MARKER,
      markerEnd:   BIDIR_MARKER,
      data:        { relationId: `${id}-r`, weight, hidden: false },
    },
  ];
}

